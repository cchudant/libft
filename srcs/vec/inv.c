#include "vec.h"

t_vec2d			vec2d_inv(t_vec2d v)
{
	v.x = 1 / v.x;
	v.y = 1 / v.y;
	return (v);
}

t_vec3d			vec3d_inv(t_vec3d v)
{
	v.x = 1 / v.x;
	v.y = 1 / v.y;
	v.z = 1 / v.z;
	return (v);
}
