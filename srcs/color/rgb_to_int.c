#include "color.h"

int		rgb_to_int(t_vec3i rgb)
{
	return (ft_min(ft_abs(rgb.x), 255) << 16 |
			ft_min(ft_abs(rgb.y), 255) << 8 |
			ft_min(ft_abs(rgb.z), 255));
}
