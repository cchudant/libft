#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		b;

	if (n < 0)
		ft_putchar_fd('-', fd);
	else
		n = -n;
	b = 1;
	while (n / b <= -10)
		b *= 10;
	while (b > 0)
	{
		ft_putchar_fd('0' + -(n / b % 10), fd);
		b /= 10;
	}
}
